            om gestolen geld te verzenden, bent u een geldezel. Geld dat ze stelen van een andere rekening, bijvoorbeeld door
            fraude, wordt op uw rekening gestort.<br />
            <br />
            Dan nemen ze geld op met je pinpas. Als geldkoerier maakt u zich schuldig aanwitwassen en moet u het gestolen geld terugbetalen.<br />
            <br />
            In dat geval kan de crimineel geen spoor achterlaten en onbewust het gesmokkelde geld weghalen. Betaalpasfraude wordt vaak besproken.<br />
